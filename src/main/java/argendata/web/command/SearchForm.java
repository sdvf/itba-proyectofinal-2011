package argendata.web.command;


public class SearchForm {

	private String type;
	private String terms;

	public SearchForm() {
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTerms() {
		return terms;
	}

	public void setTerms(String terms) {
		this.terms = terms;
	}

}
